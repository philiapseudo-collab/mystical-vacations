/**
 * Filter excursions by category
 */
export function filterExcursionsByCategory(category: string): IExcursion[] {
  if (!category || category === 'all') {
    return excursions;
  }
  return excursions.filter((exc) => exc.category === category);
}

/**
 * Get featured excursions
 */
export function getFeaturedExcursions(): IExcursion[] {
  return excursions.filter((exc) => exc.featured);
}

/**
 * Search excursions by location
 */
export function searchExcursionsByLocation(location: string): IExcursion[] {
  return excursions.filter((exc) =>
    exc.location.city.toLowerCase().includes(location.toLowerCase()) ||
    exc.location.country.toLowerCase().includes(location.toLowerCase())
  );
}

